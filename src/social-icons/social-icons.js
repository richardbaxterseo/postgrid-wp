// Social icon component wrapper
export const SocialIcon = ({ icon, url, size = '2x', className = '' }) => {
	const iconComponents = {
		facebook: FacebookIcon,
		instagram: InstagramIcon,
		twitter: TwitterIcon,
		linkedin: LinkedInIcon,
		youtube: YouTubeIcon,
		pinterest: PinterestIcon,
		rss: RSSIcon,
		email: EmailIcon,
		threads: ThreadsIcon,
	};
	
	const IconComponent = iconComponents[icon];
	
	if (!IconComponent) {
		return null;
	}
	
	const sizeClass = size ? `postgrid-social-icon--${size}` : '';
	
	return (
		<a 
			href={url} 
			className={`postgrid-social-icon postgrid-social-icon--${icon} ${sizeClass} ${className}`}
			target="_blank"
			rel="noopener noreferrer"
			aria-label={`Follow on ${icon}`}
		>
			<IconComponent />
		</a>
	);
};

